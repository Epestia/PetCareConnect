CREATE TABLE Veterinaires(
   Veterinaire_id VARCHAR(250),
   pseudo_veterinaire VARCHAR(250),
   Nom_veterinaire VARCHAR(250) NOT NULL,
   Prenom_veterinaire VARCHAR(250),
   Mail_veterinaire VARCHAR(250),
   Mot_de_passe_veterinaire VARCHAR(250),
   NumeroOrdreVeterinaires VARCHAR(250),
   PRIMARY KEY(Veterinaire_id)
);
